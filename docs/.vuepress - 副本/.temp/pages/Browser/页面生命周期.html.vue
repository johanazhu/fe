<template><h1 id="页面生命周期" tabindex="-1"><a class="header-anchor" href="#页面生命周期" aria-hidden="true">#</a> 页面生命周期</h1>
<p>DOMContentLoaded，load，beforeunload，unload</p>
<p>HTML 页面的生命周期包含三个重要事件：</p>
<ul>
<li>DOMContentLoaded——浏览器已完全加载 HTML，并构建了 DOM 树，但像图片和样式表之类的外部资源可能尚未加载完成</li>
<li>load——浏览器不仅加载完成了HTML，还加载完成了所有外部资源：图片、字体、样式等</li>
<li>beforeunload/unload——当用户正在离开页面时</li>
</ul>
