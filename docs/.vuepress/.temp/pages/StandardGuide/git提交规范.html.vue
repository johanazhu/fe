<p>-feat ：新功能</p>
<p>-fix ：修复 bug</p>
<p>-chore ：对构建或者辅助工具的更改</p>
<p>-refactor ：既不是修复 bug 也不是添加新功能的代码更改</p>
<p>-style ：不影响代码含义的更改 (例如空格、格式化、少了分号)</p>
<p>-docs ：只是文档的更改</p>
<p>-perf ：提高性能的代码更改</p>
<p>-revert ：撤回提交</p>
<p>-test ：添加或修正测试
